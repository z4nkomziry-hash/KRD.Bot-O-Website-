// Language management functions
function getCurrentLang() {
    return localStorage.getItem('tikjet_lang') || detectBrowserLang();
}

function detectBrowserLang() {
    const browserLang = (navigator.language || navigator.userLanguage).toLowerCase();
    if (browserLang.startsWith('ar')) return 'ar';
    if (browserLang.startsWith('en')) return 'en';
    if (browserLang.includes('sorani') || browserLang.includes('ckb')) return 'sorani';
    return 'badini';
}

function changeLanguage(lang) {
    const l = langData[lang] || langData['badini'];
    
    // Set document direction
    const isEnglish = (lang === 'en');
    document.documentElement.dir = isEnglish ? 'ltr' : 'rtl';
    document.documentElement.lang = isEnglish ? 'en' : (lang === 'ar' ? 'ar' : 'ku');
    
    // Update all UI elements
    const elements = {
        mainTitle: l.title,
        txtPaste: l.paste,
        txtDownload: l.download,
        txtQuickQuestion: l.quickQ,
        txtGuideTitle: l.guideT,
        txtFaqHeader: l.faqHead,
        rateThanks: l.thanks,
        txtFollowers: l.followers,
        txtHearts: l.hearts,
        txtAlbum: l.album,
        txtHistoryTitle: l.history,
        txtTrendingSectionTitle: l.trendingTitle,
        txtShareSectionTitle: l.shareTitle,
        txtPwaDesc: l.pwaDesc,
        txtPwaLater: l.pwaLater,
        btnPwaInstall: l.pwaInstall,
        txtIosTitle: l.iosTitle,
        txtIosSubtitle: l.iosSubtitle,
        txtIosStep1: l.iosStep1,
        txtIosStep2: l.iosStep2,
        txtIosStep3: l.iosStep3,
        txtIosThanks: l.iosThanks,
        txtQualitySelect: `<i class="fa-solid fa-sliders"></i> ${l.qualitySel}`,
        txtQuickDl: l.quickDl,
        txtAudioDl: l.audioDl,
        txtDlAll: l.dlAll,
        faqQ1: l.faqQ1,
        faqA1: l.faqA1,
        footerCredit: l.footer,
        developerTitle: l.developerTitle,
        developerDesc: l.developerDesc,
        githubLink: l.githubText,
        contactLink: l.contactText,
        txtPromoBtn: l.promoBtn
    };
    
    // Apply text changes
    for (const [id, text] of Object.entries(elements)) {
        const el = document.getElementById(id);
        if (el) {
            if (id === 'txtQualitySelect') {
                el.innerHTML = text;
            } else {
                el.textContent = text;
            }
        }
    }
    
    // Update placeholder based on mode
    const input = document.getElementById('tiktokUrl');
    if (input) {
        input.placeholder = currentMode === 'link' ? l.placeholder : l.placeholderUser;
    }
    
    // Update theme button text
    const isLight = document.body.classList.contains('light-mode');
    document.getElementById('txtTheme').textContent = isLight ? l.themeLight : l.themeDark;
    
    // Update step texts
    if (document.getElementById('step1')) document.getElementById('step1').textContent = l.step1;
    if (document.getElementById('step2')) document.getElementById('step2').textContent = l.step2;
    if (document.getElementById('step3')) document.getElementById('step3').textContent = l.step3;
    
    // Save preference
    localStorage.setItem('tikjet_lang', lang);
    currentLang = lang;
}
